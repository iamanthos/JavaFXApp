package Models;

public class User {
    
    private String userName;
    
    public String getUser(){
        return userName;
    }
    
    public void setUser(String userName){
        this.userName = userName;
    }
    
    private String role;
    
    public String getRole(){
        return role;
    }
    
    public void setRole (String role){
        this.role = role;
    }
}
